public class Sumatural{

    public static void main(String[] args) {

        int num = 100, sum = 0;

        for(int i = 1; i <= num; ++i)
        {
            // sum = sum + i;
            sum += i;
            // this project is done for the testing purpose
        }

        System.out.println("Sum = " + sum);
    }
}